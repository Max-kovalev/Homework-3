
import math

print('Обчислення площі трикутника за формулою Гауса')
a = float(input('Введіть сторону трикутника а:'))
b = float(input('Введіть сторону трикутника b:'))
c = float(input('Введіть сторону трикутника c:'))
p = (a + b + c) / 2
s = round(math.sqrt(p * (p - a) * (p - b) * (p - c)), 4)

print(f'Площа трикутника дорівнюе: {s}')
